"""
Program perulangan membaca buku dengan while
"""
Jumlah_buku = 10;
print("baca bukumu")

Jumlah_buku_sudah_dibaca = 0

while Jumlah_buku_sudah_dibaca < Jumlah_buku :
    Jumlah_buku_sudah_dibaca = Jumlah_buku_sudah_dibaca + 1
    print(f"Buku yang sudah dibaca {Jumlah_buku_sudah_dibaca} buah")
    

#while digunakan perulangan yang tidak tertentu sampai kondisi true